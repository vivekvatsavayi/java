public class Days {
	public static void main(String[] args) {
		
		
		//Switch statement is used to test a single variable for equality against a list of value called case
		
		
		//Execute different block of code based on value of condition
		
		String dayOfWeek = "Tuesday";
		
		switch(dayOfWeek){
			case "Monday" : 
				System.out.println("Today is Monday");
				break;
			case "Tuesday" : 
				System.out.println("Today is Tuesday");
				break;
			case "Wednesday" : 
				System.out.println("Today is Wednesday");
				break;
			case "Thursday" : 
				System.out.println("Today is Thursday");
				break;
			case "Friday" : 
				System.out.println("Today is Friday");
				break;
			
//switch statement is in one block right so when one is executed everything after that will be executed.
//if u want only one case to be executed u can add break line
		
		}
		
		
		
		
	}

}
